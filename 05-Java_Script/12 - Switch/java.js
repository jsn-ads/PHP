x = prompt("Digite o valor");

switch(x) {
		case "0":
			alert("x e zero");
			break;

		case "1":
			alert("x e um");
			break;

		case "2":
			alert("x e dois");
			break;

		case "3":
			alert("x e tres");
			break;

		case "4":
			alert("x e quatro");
			break;
			
		default:
			alert("valor nao encontrado");
			break;
	}